module Normalize (
    input wire [24:0] mant,
    input wire [7:0] exp,
    output wire [22:0] mant_out,
    output wire [7:0]  exp_out
);

    reg [4:0] leading_one;
    always @(*) begin
        casez (mant)
            25'b01???????????????????????: leading_one = 5'd23;
            25'b001??????????????????????: leading_one = 5'd22;
            25'b0001?????????????????????: leading_one = 5'd21;
            25'b00001????????????????????: leading_one = 5'd20;
            25'b000001???????????????????: leading_one = 5'd19;
            25'b0000001??????????????????: leading_one = 5'd18;
            25'b00000001?????????????????: leading_one = 5'd17;
            25'b000000001????????????????: leading_one = 5'd16;
            25'b0000000001???????????????: leading_one = 5'd15;
            25'b00000000001??????????????: leading_one = 5'd14;
            25'b000000000001?????????????: leading_one = 5'd13;
            25'b0000000000001????????????: leading_one = 5'd12;
            25'b00000000000001???????????: leading_one = 5'd11;
            25'b000000000000001??????????: leading_one = 5'd10;
            25'b0000000000000001?????????: leading_one = 5'd9;
            25'b00000000000000001????????: leading_one = 5'd8;
            25'b000000000000000001???????: leading_one = 5'd7;
            25'b0000000000000000001??????: leading_one = 5'd6;
            25'b00000000000000000001?????: leading_one = 5'd5;
            25'b000000000000000000001????: leading_one = 5'd4;
            25'b0000000000000000000001???: leading_one = 5'd3;
            25'b00000000000000000000001??: leading_one = 5'd2;
            25'b000000000000000000000001?: leading_one = 5'd1;
            25'b0000000000000000000000001: leading_one = 5'd0;
            default:                       leading_one = 5'd0;
        endcase
    end

    wire [4:0] shift_amt;
    wire [24:0] shifted;

    assign shift_amt = 5'd23 - leading_one;
    assign shifted = mant << shift_amt;

    assign mant_out = shifted[22:0];
    assign exp_out  = (mant == 25'd0) ? 8'd0 : exp - {3'b0, shift_amt};

endmodule